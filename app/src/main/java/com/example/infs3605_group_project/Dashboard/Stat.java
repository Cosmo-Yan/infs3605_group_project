package com.example.infs3605_group_project.Dashboard;

public class Stat {
    private int number;
    private int name;

    public Stat(int number, int name) {
        this.number = number;
        this.name = name;
    }

    public int getNumber() {
        return number;
    }

    public void setNumber(int number) {
        this.number = number;
    }

    public int getName() {
        return name;
    }

    public void setName(int name) {
        this.name = name;
    }
}
